package cn.zb.basetype;

/**
 * java 8种基本类型
 * 
 * @author chen
 *
 */
public class BaseType {

	public static void main(String[] args) {

		int a = 0;
		long b = 0L;
		short c = 0;
		char d = '0';
		float e = 0.0F;
		double f = 0.0;

		boolean g = false;

		byte h = 0;
		// 1、8种基本类型的取值范围
		a=Integer.MAX_VALUE;
		System.out.println("int 类型的最大值:"+a);
		a=Integer.MIN_VALUE;
		System.out.println("int 类型的最小值:"+a);
		
		

	}

}
